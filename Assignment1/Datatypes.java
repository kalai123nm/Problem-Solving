public class Datatypes{
    public static void main(String[] args) {
        int integer1=12;
        char character1='a';
        float float1=7.0f;  // 7.0 should be mentioned as 7.0f
        double double1=12.34;
        long long1=1332l;  // 1332 should be menstioned as 1332l
        byte byte1=1; 
        System.out.println("Integer  value: "+integer1);
        System.out.println("Character value: "+character1);
        System.out.println("Float value: "+float1);
        System.out.println("Double value: "+double1);
        System.out.println("Long value: "+long1);
        System.out.println("Byte value: "+byte1);
    }
}